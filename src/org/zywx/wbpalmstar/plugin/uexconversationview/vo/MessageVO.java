package org.zywx.wbpalmstar.plugin.uexconversationview.vo;

import java.io.Serializable;

/**
 * Created by ylt on 15/9/14.
 */
public class MessageVO implements Serializable {

    private int type;// 1-文本 2-录音
    private int from;// 1-me  2-you
    private Long timestamp;//时间戳
    private String data;// type == 1时  文本内容  type == 2 时 录音文件路径
    private long duration=-1l;//语音长度

    private int status=0;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getFrom() {
        return from;
    }

    public void setFrom(int from) {
        this.from = from;
    }

    public Long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public long getDuration() {
        return duration;
    }

    public void setDuration(long duration) {
        this.duration = duration;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
